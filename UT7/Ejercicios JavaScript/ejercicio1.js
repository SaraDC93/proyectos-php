//Calcular el doble de un numero
//Definimos la función
function calcularDoble(numero) {
    // Calculamos el doble del número
    let doble = numero * 2;
    // Devolvemos el resultado
    return doble;
}

// Ejemplo de uso
let numero = 5; // Cambia este valor para probar con otros números
let resultado = calcularDoble(numero);
console.log(`El doble de ${numero} es ${resultado}`);
